#include <stdio.h>

int main() {
    int a = 10;
    int b = 5;
    
    printf("a + b = %d\n", a + b); // Penjumlahan
    printf("a - b = %d\n", a - b); // Pengurangan
    printf("a * b = %d\n", a * b); // Perkalian
    printf("a / b = %d\n", a / b); // Pembagian
    printf("a %% b = %d\n", a % b); // Modulus (sisa pembagian)

    return 0;
}